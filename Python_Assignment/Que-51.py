#  How Do You Traverse Through a Dictionary Object in Python?

d1 = {'a': 1, 'b': 2, 'c': 3}


# Traverse through keys
for k in d1.keys():
    print(k)  

# Traverse through values
for v in d1.values():
    print(v)

# Traverse through keys and values
for k,v in d1.items():
    print(f"{k} = {v}")




    