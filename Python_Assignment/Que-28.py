#  Differentiate between append () and extend () methods? 

# append() : adds a sigle value in list at the end.

l1 = ["sangani",3,4]
l1.append("mital")
print(l1)

# ============================================================================================


# extend() : adds a multiple values in list (seperatly) at the end.

l1 = ["a","b",2,4]
l1.extend(["c", "d"])
print(l1)





